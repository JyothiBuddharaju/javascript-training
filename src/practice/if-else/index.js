/**
 * #### 1 ####
 * 
 * Write a function that accepts someone's age and
 * determines if there are over the legal age
 * If they are 18 and over the function 
 * should return 'No Challenge Required'
 * If they are not then the function 
 * should return 'Challenge Required'
 * 
 * NOTE: call the function legalAgeChecker
 */

export const legalAgeChecker = (age) => {
  if (age >= 18) {
    return 'No Challenge Required'
  } else {
    return 'Challenge Required'
  }
}

/**
 * #### 2 ####
 * 
 * Write a function that validates an input string
 * based on the length of the string.
 * An input is invalid if the string length
 * is greater than 10 in which case output false
 * and when the string is valid output true
 * 
 * NOTE: call the function isValidString
 */

export const isValidString = (inputString) => {
  if (inputString.length > 10) {
    return false
  } else {
    return true
  }
}